function bubbleSort(array){
    var maxLength = array.length;
    for(var i = maxLength -1; i>=0; i--){
        for(var j= maxLength - i; j>=0; j--){
            if(array[j] < array[j-1]){
                swap(array, j, j-1);
            }
        }

   }
return array;
}